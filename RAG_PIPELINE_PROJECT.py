ANALYST_KW    = {"revenue","metric","performance","q3","q4","target","expense","sales"}
RESEARCHER_KW = {"policy","rule","compliance","gdpr","process","how","contact","must"}
WRITER_KW     = {"summary","report","write","brief","explain","roadmap","overview"}

def route(query):
    w = set(re.findall(r'\b\w+\b', query.lower()))
    a = len(w & ANALYST_KW)
    r = len(w & RESEARCHER_KW)
    wr = len(w & WRITER_KW)
    if a >= r and a >= wr:   return "analyst"
    elif r >= wr:             return "researcher"
    else:                     return "writer"

def run_agent_pipeline(query):
    trace  = []
    rte    = route(query)
    trace.append(f"Supervisor → {rte}")
    docs   = retrieve(query, k=4)
    trace.append(f"Retriever  → {len(docs)} chunks")
    ans, src = extract_answer(query, docs)
    draft  = f"REPORT: {ans}"
    trace.append(f"Writer     → {len(draft)} chars drafted")
    # Validator
    ans_w  = set(re.findall(r'\b\w{5,}\b', ans.lower()))
    ctx_w  = set(re.findall(r'\b\w{5,}\b', " ".join([d["text"] for d in docs]).lower()))
    gnd    = round(len(ans_w & ctx_w) / (len(ans_w)+1e-9), 3)
    passed = gnd >= 0.25
    trace.append(f"Validator  → grounding={gnd:.3f} {'PASS' if passed else 'FAIL'}")
    return {"query": query, "route": rte, "answer": ans, "sources": src,
            "grounding": gnd, "passed": passed, "trace": trace}

