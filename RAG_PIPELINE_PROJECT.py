def extract_answer(query, retrieved):
    q_words = set(re.findall(r'\b\w{4,}\b', query.lower()))
    sents = []
    for r in retrieved:
        for s in re.split(r'[.!?]', r["text"]):
            s = s.strip()
            if len(s) > 20:
                ov = len(q_words & set(re.findall(r'\b\w{4,}\b', s.lower())))
                sents.append((ov + r["score"], s, r["meta"]["doc_id"]))
    sents.sort(reverse=True)
    if not sents:
        return "Insufficient context.", []
    top     = sents[:3]
    answer  = ". ".join([t[1] for t in top]) + "."
    sources = list(set([t[2] for t in top]))
    return answer, sources

