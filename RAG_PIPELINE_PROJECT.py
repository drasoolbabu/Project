def retrieve(query, k=4, dept=None):
    qv = model.encode([query], normalize_embeddings=True)
    scores, ids = index.search(qv.astype(np.float32), k*3)
    out = []
    for s, i in zip(scores[0], ids[0]):
        if i == -1: continue
        m = all_metadata[i]
        if dept and m["department"].lower() != dept.lower(): continue
        out.append({"text": all_chunks[i], "score": float(s), "meta": m})
        if len(out) >= k: break
    return out

