def cos(a, b): return float(np.dot(a, b)/(np.linalg.norm(a)*np.linalg.norm(b)+1e-9))

eval_results = []
for item in EVAL:
    docs   = retrieve(item["q"], k=5)
    ans, _ = extract_answer(item["q"], docs)
    ctxs   = [d["text"] for d in docs]

    aw     = set(re.findall(r'\b\w{5,}\b', ans.lower()))
    ctxw   = set(re.findall(r'\b\w{5,}\b', " ".join(ctxs).lower()))
    gtw    = set(re.findall(r'\b\w{5,}\b', item["gt"].lower()))

    faith  = round(len(aw & ctxw)/(len(aw)+1e-9), 3)
    qe     = model.encode([item["q"]], normalize_embeddings=True)[0]
    ae     = model.encode([ans],       normalize_embeddings=True)[0]
    relev  = round(cos(qe, ae), 3)
    q_prec = sum(1 for d in docs if cos(qe, model.encode([d["text"]], normalize_embeddings=True)[0]) > 0.30)
    prec   = round(q_prec/len(docs), 3) if docs else 0
    recall = round(len(gtw & ctxw)/(len(gtw)+1e-9), 3)
    overall = round(0.30*faith + 0.30*relev + 0.20*prec + 0.20*recall, 3)

    r = {"id":item["id"],"department":item["dept"],"question":item["q"],
         "generated_answer":ans[:200],"ground_truth":item["gt"],
         "faithfulness":faith,"answer_relevancy":relev,
         "context_precision":prec,"context_recall":recall,
         "overall_score":overall,"pass":overall>=0.50}
    eval_results.append(r)
    status = "✓" if r["pass"] else "✗"
    print(f"  [{status}] {item['id']} | {item['dept']:12} | F={faith:.2f} AR={relev:.2f} CP={prec:.2f} CR={recall:.2f} | {overall:.2f}")

