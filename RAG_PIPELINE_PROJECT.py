"""### Helper Function for Similarity Search

The following function `similarity_search` will take a query string, encode it using the same `SentenceTransformer` model used for indexing, and then perform a similarity search against the FAISS index. It retrieves the top `k` most similar chunks and combines them with their corresponding metadata.
"""

def similarity_search(query: str, index: faiss.Index, model: SentenceTransformer, chunks: list, metadata: list, k: int = 5) -> list:
    """
    Performs a similarity search on the FAISS index given a query.

    Args:
        query (str): The search query.
        index (faiss.Index): The FAISS index.
        model (SentenceTransformer): The sentence transformer model.
        chunks (list): List of all text chunks.
        metadata (list): List of metadata for all chunks.
        k (int): The number of top results to return.

    Returns:
        list: A list of dictionaries, each containing 'text', 'score', and 'metadata' for the top k results.
    """
    query_embedding = model.encode([query], normalize_embeddings=True).astype(np.float32)
    distances, indices = index.search(query_embedding, k)

    results = []
    for i in range(k):
        idx = indices[0][i]
        score = distances[0][i]
        results.append({
            "text": chunks[idx],
            "score": float(score),
            "metadata": metadata[idx]
        })
    return results

